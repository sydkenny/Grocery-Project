function myFunction() 
{
	if (text.style.fontSize == "14pt") 
	{
		 text.style.fontSize = "10pt";
		 text.style.fontFamily = "Times Roman";
		 text.style.color = "red";
	}
	else 
	{
		 text.style.fontSize = "14pt";
		 text.style.fontFamily = "Comic Sans MS";
		 text.style.color = "green";
	}
}